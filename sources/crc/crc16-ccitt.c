	test ("\x80\x00", 3);

	test ("\x00\x01\x00", 4);
	test ("\x00\x02\x00", 4);
	test ("\x00\x04\x00", 4);
	test ("\x00\x08\x00", 4);
	test ("\x00\x10\x00", 4);
	test ("\x00\x20\x00", 4);
	test ("\x00\x40\x00", 4);
	test ("\x00\x80\x00", 4);

